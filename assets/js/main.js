class City{
  constructor(city, date, time, humidity, pressure, wind, temp, feelLike, cloud) {
    this.city = city;
    this.date = date;
    this.time = time;
    this.humidity = humidity;
    this.pressure = pressure;
    this.wind = wind;
    this.temp = temp;
    this.feelLike = feelLike;
    this.cloud = cloud;
  }
}